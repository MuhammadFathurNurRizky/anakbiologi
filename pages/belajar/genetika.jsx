                <h3 className="text-center mt-2 font-bold xl:mt-4 xl:text-2xl">
                    Pemilahan Bebas Pada Kromosom
                </h3>
                <img
                    className="mt-2 object-contain w-full"
                    src="https://res.cloudinary.com/dxuvn93uw/image/upload/v1610529507/pemilahan_bebas_pada_kromosm_yonqxp.jpg"
