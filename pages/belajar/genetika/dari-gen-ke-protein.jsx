                <h3 className="font-bold text-center mt-2 xl:mt-4 xl:text-2xl">
                    Pengikatan RNA Polimerase Dan Inisiasi Transkripsi
                </h3>
                <img
                    className="object-contain w-full mt-1 xl:mt-3"
