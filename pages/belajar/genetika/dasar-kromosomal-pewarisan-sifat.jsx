                <h3 className="text-center font-bold mt-2 xl:mt-4 xl:text-2xl">
                    Perubahan Struktur Kromosom
                </h3>
                <img
                    className="object-contain w-full mt-1 xl:mt-3"
                    src="https://res.cloudinary.com/dxuvn93uw/image/upload/v1610773988/perubahan_struktur_kromosom_p71di5.jpg"
